texto = 'python S2'
print(texto[5])  # Acessar o indice 5 da string no caso o n
print(texto[-1])  # Acessar o último indice da string no caso o 2
print(texto[2:6])  # Acessa a string do indice 2 até o indice 5, "o 6 não é incluso"
print(texto[-2:])  # Acessa a string do indice -2(s) até o final da string
print(texto[:3])  # Acessa astring do começo até o indice 2(t) pois o 3 não é incluso
print(texto[:6:2])  # Imprime a string do começo até o indice 5(o 6 não é incluso) pulando de 2 em 2 indices
